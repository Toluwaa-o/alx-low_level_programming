/**
 * add - Adds two integers
 * @x: The first integer
 * @y: The second integer
 *
 * Description: This function takes two integers, `a` and `b`, as input
 *              and returns their sum.
 *
 * Return: The sum of the two integers
 */

int add(int x, int y)
{
	int result = x + y;

	return (result);
}
